class Artithmetic {
    public static void main(String[] args) {
        // declare variables
        int a = 12, b = 5;
        // addition operator
        System.out.println("a + b = " + (a + b));

        // subtraction operator
        System.out.println("a - b = " + (a - b));

        // multiplication operator
        System.out.println("a * b = " + (a * b));

        // division operator
        System.out.println("a / b = " + (a / b));

        // modulo operator
        System.out.println("a % b = " + (a % b));
    }
}

// (9 / 2) ==> 4
// (9.0 / 2) ==> 4.5
// (9 / 2.0) ==> 4.5
